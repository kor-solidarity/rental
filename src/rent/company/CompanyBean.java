package rent.company;

public class CompanyBean {
	private String com_id, com_pwd, com_name, com_addr, com_tel, com_mail, sign;

	public String getCom_id() {
		return com_id;
	}

	public void setCom_id(String com_id) {
		this.com_id = com_id;
	}

	public String getCom_pwd() {
		return com_pwd;
	}

	public void setCom_pwd(String com_pwd) {
		this.com_pwd = com_pwd;
	}

	public String getCom_name() {
		return com_name;
	}

	public void setCom_name(String com_name) {
		this.com_name = com_name;
	}

	public String getCom_addr() {
		return com_addr;
	}

	public void setCom_addr(String com_addr) {
		this.com_addr = com_addr;
	}

	public String getCom_tel() {
		return com_tel;
	}

	public void setCom_tel(String com_tel) {
		this.com_tel = com_tel;
	}

	public String getCom_mail() {
		return com_mail;
	}

	public void setCom_mail(String com_mail) {
		this.com_mail = com_mail;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	
}
