package rent.maintenence;

public class MaintenenceBean {
	private String mt_id, mt_sdate, mt_edate, content, cost, car_id;

	public String getMt_id() {
		return mt_id;
	}

	public void setMt_id(String mt_id) {
		this.mt_id = mt_id;
	}

	public String getMt_sdate() {
		return mt_sdate;
	}

	public void setMt_sdate(String mt_sdate) {
		this.mt_sdate = mt_sdate;
	}

	public String getMt_edate() {
		return mt_edate;
	}

	public void setMt_edate(String mt_edate) {
		this.mt_edate = mt_edate;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getCost() {
		return cost;
	}

	public void setCost(String cost) {
		this.cost = cost;
	}

	public String getCar_id() {
		return car_id;
	}

	public void setCar_id(String car_id) {
		this.car_id = car_id;
	}
}
