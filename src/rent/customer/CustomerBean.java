package rent.customer;

public class CustomerBean {
	private String cus_id, cus_pwd, cus_name, cus_addr, cus_tel, cus_mail, license;

	public String getCus_id() {
		return cus_id;
	}

	public void setCus_id(String cus_id) {
		this.cus_id = cus_id;
	}

	public String getCus_pwd() {
		return cus_pwd;
	}

	public void setCus_pwd(String cus_pwd) {
		this.cus_pwd = cus_pwd;
	}

	public String getCus_name() {
		return cus_name;
	}

	public void setCus_name(String cus_name) {
		this.cus_name = cus_name;
	}

	public String getCus_addr() {
		return cus_addr;
	}

	public void setCus_addr(String cus_addr) {
		this.cus_addr = cus_addr;
	}

	public String getCus_tel() {
		return cus_tel;
	}

	public void setCus_tel(String cus_tel) {
		this.cus_tel = cus_tel;
	}

	public String getCus_mail() {
		return cus_mail;
	}

	public void setCus_mail(String cus_mail) {
		this.cus_mail = cus_mail;
	}

	public String getLicense() {
		return license;
	}

	public void setLicense(String license) {
		this.license = license;
	}
	
}
